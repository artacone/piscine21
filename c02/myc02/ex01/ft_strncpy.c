char	*ft_strncpy(char *dest, char *src, unsigned int n)
{
	char	*d;
	char	*s;

	d = dest;
	s = src;
	while (n && *s)
	{
		*d++ = *s++;
		n--;
	}
	while (n--)
	{
		*d++ = '\0';
	}
	return (dest);
}
