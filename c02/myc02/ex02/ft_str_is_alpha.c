int			ft_is_alpha(char c)
{
	return ((c >= 'a' && c <= 'z') || (c >= 'A' && c <= 'Z'));
}

int			ft_str_is_alpha(char *str)
{
	while (*str)
	{
		if (!ft_is_alpha(*str))
		{
			return (0);
		}
		str++;
	}
	return (1);
}
