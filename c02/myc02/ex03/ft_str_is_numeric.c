int	ft_is_numeric(char c)
{
	return (c >= '0' && c <= '9');
}

int	ft_str_is_numeric(char *str)
{
	char	*s;

	s = str;
	while (*s)
	{
		if (!ft_is_numeric(*s++))
		{
			return (0);
		}
	}
	return (1);
}
