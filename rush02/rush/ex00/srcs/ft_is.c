#include "../includes/ft.h"

int			ft_is_numeric(char c)
{
	return (c >= '0' && c <= '9');
}

int			ft_is_space(char c)
{
	if (c == ' ' || (c >= '\t' && c <= '\r'))
	{
		return (1);
	}
	return (0);
}
