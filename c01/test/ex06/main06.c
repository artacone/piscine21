#include <unistd.h>

void		ft_putchar(char c)
{
	write(1, &c, 1);
}

void		ft_putnbr(int nb)
{
	if (nb == -2147483648)
	{
		ft_putchar('-');
		ft_putchar('2');
		nb = 147483648;
	}
	if (nb < 0)
	{
		ft_putchar('-');
		nb = -nb;
	}
	if (nb < 10)
	{
		ft_putchar(nb + '0');
	}
	else
	{
		ft_putnbr(nb / 10);
		ft_putchar(nb % 10 + '0');
	}
}

int			ft_strlen(char *str);

int			main(void)
{
	char str[20] = "Hello, world!\0";
	ft_putnbr(13);
	ft_putchar('\t');
	ft_putnbr(ft_strlen(str));
	ft_putchar('\n');
	return (0);
}
