#include <unistd.h>

void		ft_putchar(char c)
{
	write(1, &c, 1);
}

void		ft_putnbr(int nb)
{
	if (nb == -2147483648)
	{
		ft_putchar('-');
		ft_putchar('2');
		nb = 147483648;
	}
	if (nb < 0)
	{
		ft_putchar('-');
		nb = -nb;
	}
	if (nb < 10)
	{
		ft_putchar(nb + '0');
	}
	else
	{
		ft_putnbr(nb / 10);
		ft_putchar(nb % 10 + '0');
	}
}

void		ft_ft(int *nbr);

int			main(void)
{
	int n;
	int *pn;

	n = 0;
	pn = &n;
	ft_putnbr(*pn);
	ft_putchar('\n');
	ft_ft(pn);
	ft_putnbr(*pn);
	ft_putchar('\n');
	return (0);
}
