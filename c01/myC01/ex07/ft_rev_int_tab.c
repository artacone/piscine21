void		ft_swap(int *a, int *b)
{
	int t;

	t = *a;
	*a = *b;
	*b = t;
}

void		ft_rev_int_tab(int *tab, int size)
{
	int i;

	i = 0;
	while (i != size - 1)
	{
		ft_swap(tab + i, tab + size - 1);
		i++;
		size--;
	}
}
